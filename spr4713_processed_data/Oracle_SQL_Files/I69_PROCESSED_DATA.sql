CREATE TABLE I69_PROCESSED_DATA (
id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
filename_id NUMBER NOT NULL,
PROJECT_NAME VARCHAR2(10 BYTE) NOT NULL,
TEST_SECTION NUMBER(5,0) NOT NULL,
SENSOR_TYPE	VARCHAR2(14 BYTE) NOT NULL,
LOCATION NUMBER(5,2) NOT NULL,
GAGEID	NUMBER(6,0) NOT NULL,
SURVEY_DATE VARCHAR2(10 BYTE) NOT NULL,
DESCRIPTION	VARCHAR2(5 BYTE) NOT NULL,
CAL_FACTOR NUMBER(8,4) NOT NULL,
B_VALUE NUMBER(7,5) NOT NULL,
X NUMBER(8,5) NOT NULL,
Y NUMBER(12,5) NOT NULL,
EXTREMA VARCHAR2(7 BYTE) NOT NULL,
LOAD_LB NUMBER(5,0) NOT NULL, 
PRIMARY KEY(id),
constraint i69_enumerate_sensor_type CHECK (SENSOR_TYPE in ('PRESSURE CELL', 'STRAIN GAGE')),
constraint i69_enumerate_extrema CHECK (EXTREMA in ('maxima', 'minima')),
constraint i69_unique_data UNIQUE (PROJECT_NAME,TEST_SECTION,SENSOR_TYPE,LOCATION, GAGEID, SURVEY_DATE,EXTREMA, LOAD_LB),
constraint i69_fk_field_filename foreign key (filename_id) references I69_FILE_INFO (ID)
);
